#include <stdio.h>

int main() {

// Entradas
    int numero = 10;
    float numeroDecimal = 50.49;
    char letra = 'A';
    char nome[6] = "Lidia";
    
// Saidas
    printf("Numero: %d\n", numero);
    printf("Decimal: %f\n", numeroDecimal);
    printf("Letra: %c\n", letra);
    printf("Nome: %s\n", nome);

    return 0;
}